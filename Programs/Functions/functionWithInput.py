# Simple Function
def greet():
    print('Hello')
    print('How are you?')
    print('Bye')


greet()

# Function with Input


def greet_with_name(name):
    print(f'Hello {name}')
    print(f'How are you {name}?')
    print(f'Bye {name}')


greet_with_name('Aman')
greet_with_name('Sumit')
greet_with_name('Harsha')
